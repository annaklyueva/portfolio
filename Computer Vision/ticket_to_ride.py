def city_center(img_gray, template=template_all, threshold = 0.65):
    width, high = template.shape[::-1]

    res = cv2.matchTemplate(img_gray, template, cv2.TM_CCOEFF_NORMED)
    
    lable, n = label(res >= threshold, connectivity=2, return_num=True)
    lable = np.int16([np.round(np.mean(np.argwhere(lable == i), axis=0)) for i in range(1, n + 1)])
    
    centers = [[pt[0]+width//2, pt[1]+high//2] for pt in lable]
    
    
    return centers


